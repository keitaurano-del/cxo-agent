export const PIVOT_TIPS_MD = `## よく使う操作

- 表示形式：値を右クリック →［表示形式］で、桁区切り（#,##0）や％にすると一気に見やすくなります。
- グループ化：日付の項目を行に置き、右クリック →［グループ化］で「月」「四半期」単位にまとめられます。計上月別の推移を見るときに使います。
- 並べ替え：値の列で右クリック →［並べ替え］で、金額の大きい順などに並べられます。
- 更新：元データを変えたら、ピボット上で右クリック →［更新］。自動では更新されない点に注意してください。

## この案件での使いどころ

- 支店別・業種別・債務者区分別・ステージ別の EAD／ECL 集計。
- 計上月別の引当（ECL）の推移。
- クロス集計（債務者区分 × ステージ）で、どこに引当が偏っているかの把握。
- ステージ 2・3 だけに絞った支店別の状況点検。

いずれも、ピボットなら数分で表にできます。ECL の点検・報告の下ごしらえに有効です。`;

